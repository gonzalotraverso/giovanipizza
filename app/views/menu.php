<script>
$$('.app-section.app-section-menu .content .item').each(function(item, index){
  item.getElements('.item-options .item-option').each(function(opt,opti){  
    opt.addEvent('click', function(){
      item.getElements('.item-options .item-option.active').removeClass('active');
      item.getElements('.option-change.active').removeClass('active');
      item.getElements('.option-change.option-' + (opti+1) ).addClass('active');      
      this.addClass('active');
    });
  });
  item.getElement('.item-head').addEvent('click', function(){
    App.Helper.changeAppClass('menu menu-' + (index+1) );
    if(item.hasClass('active'))
    {
      $$('.app-section.app-section-menu .content .item.active').removeClass('active');
      $$('.app-section.app-section-menu .content .item.inactive').removeClass('inactive');
      return;
    }
    $$('.app-section.app-section-menu .content .item.active').removeClass('active');
    $$('.app-section.app-section-menu .content .item').addClass('inactive');
    item.removeClass('inactive').addClass('active');
  });
});
</script>
